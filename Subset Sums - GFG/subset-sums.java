class Solution{
    ArrayList<Integer> subsetSums(ArrayList<Integer> arr, int N){
        // code here
        ArrayList<Integer> list=new ArrayList<>();
        f(list,0,arr,N-1);
        return list;
    }
    public void f(ArrayList<Integer> list,int sum,ArrayList<Integer> arr,int ind){
        if(ind==-1){
            list.add(sum);
            return;
        }
        f(list,sum,arr,ind-1);
        f(list,sum+arr.get(ind),arr,ind-1);
    }
}